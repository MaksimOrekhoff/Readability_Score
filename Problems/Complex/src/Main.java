class Complex {

    private double real;
    private double imaginary;
    
    public double getReal() {
        return real;
    }
    public double getImaginary() {
        return imaginary;
    }
    public void setReal(double a) {
        this.real = a;
    }
    public void setImaginary(double b) {
        this.imaginary = b;
    }

}